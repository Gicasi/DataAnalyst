#clear() = limpa a lista
vendedores = ["Gicasi", "Maria", "João", "Ana", "Carlos"]
print(vendedores)
vendedores.clear() #limpa a lista
print(vendedores)

#remove() = remove um item específico da lista
vendedores = ["Gicasi", "Maria", "João", "Ana", "Carlos"]
vendedores.remove("João") #removendo o "João"
print(vendedores)   
